package VO;

public class stateVO {
	private int id;
	private String sn;
	private String sd;
	private countryVO c;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getSn() {
		return sn;
	}
	public void setSn(String sn) {
		this.sn = sn;
	}
	public String getSd() {
		return sd;
	}
	public void setSd(String sd) {
		this.sd = sd;
	}
	public countryVO getC() {
		return c;
	}
	public void setC(countryVO c) {
		this.c = c;
	}
	

}
